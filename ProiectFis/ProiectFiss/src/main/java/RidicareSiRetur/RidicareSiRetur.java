    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try
     {
        String url = "jdbc:mysql://localhost:3306/proiectfis";
	Statement sql;
	ResultSet rs;
	Connection con1 = DriverManager.getConnection(url, "root", "Analyst123");
	sql = (Statement) con1.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
	rs = sql.executeQuery("select * from comenzi");
        rs.next();
        int ok=0;
        int pretintermediar=0;
        int pretcrescut=0;
        int greutateintermediara=0;
        int nrzile=0;
        String tipcolet="";
        do
        {
            if(rs.isLast())ok=1;
            if(rs.getString("NrColet").equals(CautNrColet.getText()))
            {
                CautStatus.setText(ComboStatus.getSelectedItem().toString());
                if(rs.getString("status").contains("In Prelungire") && ComboStatus.getSelectedItem().equals("Ridicat"))
                {
                  pretintermediar=Integer.parseInt(CautPret.getText());
                  greutateintermediara=Integer.parseInt(CautGreutate.getText());
                  nrzile=Integer.parseInt(jTextField1.getText());
                  tipcolet=CautTip.getText();
                  if(tipcolet.equals("Periculos"))pretcrescut=pretcrescut + (nrzile * (4*greutateintermediara));
                          else if (tipcolet.equals("Pretios"))pretcrescut= pretcrescut + (nrzile * (3*greutateintermediara));
                          else pretcrescut= pretcrescut + (nrzile *(2*greutateintermediara));
                  pretintermediar=pretintermediar+pretcrescut;
                  rs.updateString("pret",Integer.toString(pretintermediar));
                  CautPret.setText(Float.toString(pretintermediar));
                  JOptionPane.showConfirmDialog(null, "S-a aplicat taxa de " + Integer.toString(pretcrescut)+ " " + "pentru " + Integer.toString(nrzile) + " zile intarziere");
                }       
                rs.updateString("status", ComboStatus.getSelectedItem().toString());
                rs.updateRow();
            }
            rs.next();
        }while(ok==0);
      }
         catch(SQLException ex)
    {
         JOptionPane.showMessageDialog(null, ex);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

