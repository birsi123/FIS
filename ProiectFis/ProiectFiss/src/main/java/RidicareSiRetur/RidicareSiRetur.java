     try
     {
        String url = "jdbc:mysql://localhost:3306/proiectfis";
	Statement sql;
	ResultSet rs;
	Connection con1 = DriverManager.getConnection(url, "root", "Analyst123");
	sql = (Statement) con1.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
	rs = sql.executeQuery("select * from comenzi");
        rs.next();
        int ok=0;
        do
        {
            if(rs.isLast())ok=1;
            if(rs.getString("NrColet").equals(CautNrColet.getText()))
            {
                CautSursa.setText(rs.getString("Sursa"));
                CautDest.setText(rs.getString("Destinatie"));
                CautTip.setText(rs.getString("CategorieColet"));
                CautStatus.setText(rs.getString("status"));
                CautPret.setText(rs.getString("pret"));
                CautGreutate.setText(rs.getString("greutate"));
            }
            rs.next();
        }while(ok==0);
        
			
     }
     catch(SQLException ex)
    {
         JOptionPane.showMessageDialog(null, ex);
    }
